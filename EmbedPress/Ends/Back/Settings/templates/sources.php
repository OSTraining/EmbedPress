    document.addEventListener("DOMContentLoaded", function() {
        // Get all tab buttons and tab content items
        var tabButtons = document.querySelectorAll(".tab-button");
        var tabItems = document.querySelectorAll(".source-item");

        // Add click event listeners to each tab button
        tabButtons.forEach(function(button) {
            button.addEventListener("click", function() {
                // Remove "active" class from all tab buttons
                tabButtons.forEach(function(btn) {
                    btn.classList.remove("active");
                });

                // Add "active" class to the clicked tab button
                button.classList.add("active");

                // Get the data-tab attribute of the clicked button
                var tabName = button.getAttribute("data-tab");

                // Show or hide tab content items based on the clicked button
                tabItems.forEach(function(item) {
                    var dataTab = item.getAttribute("data-tab");
                    if (tabName === "all" || tabName === dataTab) {
                        item.style.display = "flex";
                    } else {
                        item.style.display = "none";
                    }
                });
            });
        });

        // Initialize by clicking the "All Sources" tab
        var allButton = document.querySelector(".tab-button[data-tab='all']");
        if (allButton) {
            allButton.click();
        }
    });
</script>

